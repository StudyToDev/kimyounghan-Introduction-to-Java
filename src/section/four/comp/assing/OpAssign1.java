package section.four.comp.assing;

public class OpAssign1 {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a);

        a += 3;
        System.out.println(a);

        a -= 2;
        System.out.println(a);

        a *= 4;
        System.out.println(a);

        a /= 3;
        System.out.println(a);

        a %= 5;
        System.out.println(a);
    }
}
